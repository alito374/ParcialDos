public class NumerosPares {
    public static void main(String[] args) {
        // La variable 'N' se declara y utiliza directamente en el ciclo for.
        // Cumple con el requisito de ser la única variable.
        for (int N = 0; N <= 100; N += 2) {
            // Imprime el valor de N en una nueva línea
            System.out.println(N);
        }
    }
}